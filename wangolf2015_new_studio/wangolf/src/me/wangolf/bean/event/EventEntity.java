import java.io.Serializable;
import java.util.ArrayList;

public class EventEntity implements Serializable {

	private static final long serialVersionUID = 1L;

	private int id;
	private int pranum;// 赞数
	private int submit;// 0表示不可以报名，1表示可以报名
	private String title;
	private String btime;// 开始时间
	private String etime;// 结束时间 2015-05-05 00:00:00
	private String address;
	private String price;// 价格
    private String origin_price;//原价
	private String logo;// 图片
	private String status;
	private String info;
	private ArrayList<EventEntity> data;
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getSubmit() {
		return submit;
	}

	public void setSubmit(int submit) {
		this.submit = submit;
	}

	public int getPranum() {
		return pranum;
	}

	public void setPranum(int pranum) {
		this.pranum = pranum;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getBtime() {
		return btime;
	}

    public String getOrigin_price() {
        return origin_price;
    }

    public void setOrigin_price(String origin_price) {
        this.origin_price = origin_price;
    }

    public void setBtime(String btime) {
		this.btime = btime;
	}

	public String getEtime() {
		return etime;
	}

	public void setEtime(String etime) {
		this.etime = etime;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getLogo() {
		return logo;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public ArrayList<EventEntity> getData() {
		return data;
	}

	public void setData(ArrayList<EventEntity> data) {
		this.data = data;
	}

}
