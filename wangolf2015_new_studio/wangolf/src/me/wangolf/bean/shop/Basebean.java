package me.wangolf.bean.shop;

import java.util.ArrayList;

public class Basebean {
	private String status;
	private String info;
	private ArrayList<Pro> data;
	private ArrayList<PageInfo> pageinfo;

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public ArrayList<Pro> getData() {
		return data;
	}

	public void setData(ArrayList<Pro> data) {
		this.data = data;
	}

	public ArrayList<PageInfo> getPageinfo() {
		return pageinfo;
	}

	public void setPageinfo(ArrayList<PageInfo> pageinfo) {
		this.pageinfo = pageinfo;
	}

}
