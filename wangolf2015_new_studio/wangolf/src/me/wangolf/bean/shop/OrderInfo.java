import java.io.Serializable;

public class OrderInfo implements Serializable {

	private static final long serialVersionUID = 1L;

	private String uid; // 用户iD
	private String vsn;// 代金券
	private String cid;// 供应商ID
	private int proid;// 商品ID
	private String mobile;// 电话号码
	private String number;// 购买数量
	private String address;// 收货人地址
	private String cusname;// 联人人
	private String zip;// 邮编
	private String proattr;// 商品属性
	private String oamount;// 订单总金额
	private String payment;// 支付方法支付方式（1、银联付款2、支付宝3、微信支付,4余额支付,5代金券支付）
	private String book;// 备注信息
	private String pamount;// 需要支付的金额(总金额-代金券-余额)
	private String samount;// 自己余额(若填写必须是自己的全部余额，用户不可修改)
	private String vamount;// 代金券价值(通过后端查看获取)
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getVsn() {
		return vsn;
	}
	public void setVsn(String vsn) {
		this.vsn = vsn;
	}
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public int getProid() {
		return proid;
	}
	public void setProid(int proid) {
		this.proid = proid;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getCusname() {
		return cusname;
	}
	public void setCusname(String cusname) {
		this.cusname = cusname;
	}
	public String getZip() {
		return zip;
	}
	public void setZip(String zip) {
		this.zip = zip;
	}
	public String getProattr() {
		return proattr;
	}
	public void setProattr(String proattr) {
		this.proattr = proattr;
	}
	public String getOamount() {
		return oamount;
	}
	public void setOamount(String oamount) {
		this.oamount = oamount;
	}
	public String getPayment() {
		return payment;
	}
	public void setPayment(String payment) {
		this.payment = payment;
	}
	public String getBook() {
		return book;
	}
	public void setBook(String book) {
		this.book = book;
	}
	public String getPamount() {
		return pamount;
	}
	public void setPamount(String pamount) {
		this.pamount = pamount;
	}
	public String getSamount() {
		return samount;
	}
	public void setSamount(String samount) {
		this.samount = samount;
	}
	public String getVamount() {
		return vamount;
	}
	public void setVamount(String vamount) {
		this.vamount = vamount;
	}

}
